

import re

t = "2022-09-24"

print(re.match('(\d{4})-(0[1-9]|[0-2])-([0-2]\d|[3][0-1])',t).group(2))
#09
print(re.match('(\d{4})-(0[1-9]|[0-2])-([0-2]\d|[3][0-1])',t).groups(2))
#('2022', '09', '24') 元祖
print(re.match('(\d{4})-(0[1-9]|[0-2])-([0-2]\d|[3][0-1])',t).groups(2))

print(re.match('(\d{4})-(0[1-9]|[0-2])-([0-2]\d|[3][0-1])',t).start())
#0
print(re.match('(\d{4})-(0[1-9]|[0-2])-([0-2]\d|[3][0-1])',t).end())
#10

print(re.match('(\d{4})-(0[1-9]|[0-2])-([0-2]\d|[3][0-1])',t).span())
#(0, 10)

print(re.match('(\d{4})-(0[1-9]|[0-2])-([0-2]\d|[3][0-1])',t).string)
#2022-09-24

print(re.search('(\d{4})-(0[1-9]|[0-2])-([0-2]\d|[3][0-1])',t))
#<re.Match object; span=(0, 10), match='2022-09-24'>

print(re.search('a', 'ba1a2'))
#<re.Match object; span=(1, 2), match='a'>

print(re.split('(\d{4})-(0[1-9]|[0-2])-([0-2]\d|[3][0-1])',t))
#['', '2022', '09', '24', '']

print(re.sub('-','/', t))
#2022/09/24

print(re.sub('-', '/', t, count=(1)))
#2022/09-24